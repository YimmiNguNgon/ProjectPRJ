package entity;

/**
 *
 * @author ADMIN
 */
public class Denomination {
    private int gid;
    private String gname;

    public Denomination() {
    }


    public Denomination(int gid, String gname) {
        this.gid = gid;
        this.gname = gname;
    }

    public int getGid() {
        return gid;
    }

    public void setGid(int gid) {
        this.gid = gid;
    }

    public String getGname() {
        return gname;
    }

    public void setGname(String gname) {
        this.gname = gname;
    }
    
}
